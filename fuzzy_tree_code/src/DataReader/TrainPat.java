package DataReader;

public class TrainPat {

    private double x[];
    private double y[];
    private double t;

    public double[] getX() {
        return x;
    }

    public void setX(double x[]) {
        this.x = x;
    }

    public double[] getY() {
        return y;
    }

    public void setY(double y[]) {
        this.y = y;
    }

    public double getCls() {
        return t;
    }

    public void setCls(double t) {
        this.t = t;
    }
}
